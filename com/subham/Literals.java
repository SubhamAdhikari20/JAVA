package com.subham;

public class Literals {
    public static void main(String[] args) {
        byte age1 = 19;     // Automatically, the value is stored as byte
        int age2 = 45;      // Automatically, the value is stored as int
        short age3 = 78;    // Automatically, the value is stored as short
        long  age4 = 1256565654536L;    // by default, it is integer (int)
        char ch = 'S';      // Automatically, the value is stored as char
        float f1 = 5.5f;    // by default, it is float
        double d1 = 5.587;  // Default
        boolean a = true;
        String str = "Subham";
        System.out.println(age1);
        System.out.println(ch);
    }
}
